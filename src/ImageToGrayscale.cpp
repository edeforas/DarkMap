#include "ImageToGrayscale.h"	
//////////////////////////////////////////////////////////////////////////////
ImageToGrayscale::ImageToGrayscale()
{ }
//////////////////////////////////////////////////////////////////////////////
ImageToGrayscale::~ImageToGrayscale()
{ }
//////////////////////////////////////////////////////////////////////////////
void ImageToGrayscale::compute(const cv::Mat & mIn,cv::Mat & mOut)
{
    if(mIn.channels()!=1)
        cv::cvtColor(mIn, mOut, cv::COLOR_RGB2GRAY);
    else
        mOut=mIn;
}
